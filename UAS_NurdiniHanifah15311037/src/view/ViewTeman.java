    private void jB_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_simpanActionPerformed
        // TODO add your handling code here:
        try {
            String nama = jT_nama.getText();
            String nope = jT_nope.getText();
            String email = jT_email.getText();
            mt.setNama(nama);
            mt.setNope(nope);
            mt.setEmail(email);
            ct.simpanData(mt);
            datatable();
            bersih();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Silahkan Masukkan Angka");
            jT_nope.setText("");
            jT_nope.requestFocus();
        }
    }//GEN-LAST:event_jB_simpanActionPerformed

