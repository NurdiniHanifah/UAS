    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        try {
            int baris = jTable1.getSelectedRow();
            jT_id.setText(jTable1.getValueAt(baris, 0).toString());
            jT_nama.setText(jTable1.getValueAt(baris, 1).toString());
            jT_nope.setText(jTable1.getValueAt(baris, 2).toString());
            jT_email.setText(jTable1.getValueAt(baris, 3).toString());
        } catch (Exception e) {
            System.err.println("Error : " + e);
        }
    }//GEN-LAST:event_jTable1MouseClicked

